function newViewState = setViewerState(this, newViewState)

previousState = this.State;
if isempty(previousState) || ~ishandle(previousState) % Applying ishandle to a non-HG object
    % do nothing
elseif isa(previousState,'MapViewer.DataTipState')
    this.PreviousDataTipState = previousState;
else
    delete(previousState)
